#include <iostream>

using namespace std;

string m[7][10] = {
    {"+---+", "    +", "+---+", "+---+", "+   +", "+---+", "+---+", "+---+",
     "+---+", "+---+"},
    {"|   |", "    |", "    |", "    |", "|   |", "|    ", "|    ", "    |",
     "|   |", "|   |"},
    {"|   |", "    |", "    |", "    |", "|   |", "|    ", "|    ", "    |",
     "|   |", "|   |"},
    {"+   +", "    +", "+---+", "+---+", "+---+", "+---+", "+---+", "    +",
     "+---+", "+---+"},
    {"|   |", "    |", "|    ", "    |", "    |", "    |", "|   |", "    |",
     "|   |", "    |"},
    {"|   |", "    |", "|    ", "    |", "    |", "    |", "|   |", "    |",
     "|   |", "    |"},
    {"+---+", "    +", "+---+", "+---+", "    +", "+---+", "+---+", "    +",
     "+---+", "+---+"},
};

int main() {
    string s;

    int i;
    while (cin >> s && s != "end") {
        i = 0;
        for (int i = 0; i < 7; i++) {
            for (int j = 0; j < s.length(); j++) {
                if (j == 2) {
                    if (i == 2 || i == 4) {
                        cout << "o";
                    } else {
                        cout << " ";
                    }
                } else {
                    cout << m[i][s[j] - 48];
                }

                if (j != s.length() - 1) {
                    cout << "  ";
                }
            }
            cout << endl;
        }

        cout << endl << endl;
    }

    cout << "end" << endl;

    return 0;
}
